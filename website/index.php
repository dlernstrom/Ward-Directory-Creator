      Wards and Branches of the Church of Jesus Christ of Latter-day Saints. This application is neither created, nor endorsed by the church.</span></p>
  <p align="center"><span class="style2">The Ward Directory Creator uses the comma seperated values (csv) file, available on your ward's website, for its
